
public class Shortand {
	static public void main (String ... Sujal)
	{
		/* Shorthand assingment help to reduce code as example:-
		 * a =a +2; is write as
		 * a+=2;
		 * both answer is same*/
		
		int a = 10;
		a+=5;
		System.out.println( "a=10 add 5 = "+a);
		int b = 10;
		b-=5;
		System.out.println( "b=10 sub 5 = "+b);
		int c = 10;
		c*=5;
		System.out.println( "c=10 mul 5 = "+c);
		int d = 10;
		d/=5;
		System.out.println( "d=10 div 5 = "+d);
		int f = 10;
		f%=3;
		System.out.println( "f=10 mod 3 = "+f);
		
		
	}

}
